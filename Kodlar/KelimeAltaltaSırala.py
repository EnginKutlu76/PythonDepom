class Matematik:
    def Topla(self,sayi1,sayi2):
        return sayi1 + sayi2
    
    def Cıkart(self,sayi1,sayi2):
        return sayi1 - sayi2
    
    def Carp(self,sayi1,sayi2):
        return sayi1 * sayi2    
    
    def Bol(self,sayi1,sayi2):
        return sayi1 / sayi2    
    
matematik = Matematik()
print("Toplam = " + str(matematik.Cıkart(2, 29)))